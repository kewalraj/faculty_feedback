<h3>Modules and Features <br></h3>
<h4>1. Administrator Module <br></h4>
The administrator plays a pivotal role in managing the system and ensuring its smooth operation. Features include: <br>

<h5>a. Student Account Management: <br>
</h5>
The administrator creates student accounts by adding student information (name, roll number, username, and password). <br>
Assigns unique login credentials to each student. <br><br>

<h5>b. Feedback Management: <br>
</h5>
Monitors feedback submitted by students.
Generates reports and analyzes the results.

<h5>c. Login: <br>
</h5>
Admin can log in using a default username and password (no need for database storage).
Default Credentials: <br>
Username: admin <br>
Password: sandeep <br> <br> <br>
<h4>2. Student Module <br>
</h4>
The student module is designed for feedback submission. Features include: <br>

Login:

Students log in with their university roll number as their username and their date of birth as their password.
Select "Student" on the login page. <br> <br>
Feedback Submission: 

After logging in, students can 
View their personal details. <br>
Select their course stream, semester, and subjects.
Provide feedback for individual faculty members based on specific subjects.
Students having low attendance are restricted from providing feedback. <br><br><br>
<h4>3. Faculty Module  <br>
</h4>
The faculty module allows instructors to view feedback and update their profiles. Features include: <br>

Login: <br>

Faculty log in using their registered email and password. <br>
Profile Management: <br>

Update personal details, such as name, email, and mobile number. <br>
View Feedback: <br>

Access feedback results to analyze their performance. <br>
